// Function to get random art - unchanged
export function getRandomAsciiArt(): string {
  if (placeholders.length === 0) return "Loading..."; // Fallback
  const randomIndex = Math.floor(Math.random() * placeholders.length);
  return placeholders[randomIndex]?.trim() || "Loading...";
}